package org.github.code.algos.medium;

public class BitManipulation {
    public static void main(String[] args) {
        int num = 999999999; // Example number
        System.out.println(Integer.toBinaryString(num)); // Output: 101
        System.out.println(Long.toBinaryString(999999999999999999L)); // Output: 101
    }
}
